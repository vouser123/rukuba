import fs from "node:fs/promises";
import path from "node:path";

const repoRoot = path.resolve(import.meta.dirname, "..");
const sourcePath = path.join(repoRoot, "docs", "BEADS_OPERATIONS.md");
const outputPath = path.join(repoRoot, "docs", "BEADS_QUICKREF.md");

const source = await fs.readFile(sourcePath, "utf8");

const markerRegex = /<!-- QUICKREF:BEGIN -->([\s\S]*?)<!-- QUICKREF:END -->/g;
const sections = [];
let match;

while ((match = markerRegex.exec(source)) !== null) {
  sections.push(match[1].trim());
}

if (sections.length === 0) {
  throw new Error("No QUICKREF sections found in BEADS_OPERATIONS.md");
}

const generated = `# Beads Quick Reference

Derived from \`docs/BEADS_OPERATIONS.md\`. Do not edit this file by hand.
Regenerate with \`npm run beads:quickref\`.

${sections.join("\n\n---\n\n")}
`;

await fs.writeFile(outputPath, generated);
console.log(`Wrote ${path.relative(repoRoot, outputPath)}`);
